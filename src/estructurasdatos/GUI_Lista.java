    private void jButton_eliminar_posicionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_eliminar_posicionMouseClicked
        // TODO add your handling code here:
        int posicion = Integer.parseInt(this.jSpinner_posicion.getValue().toString());
        posicion = lista.EliminarPosicion(posicion);
        
        if (posicion == -1) {
            JOptionPane.showMessageDialog(this, "No se puedo eliminar");
        } else {
            model_lista.remove(posicion);
        }
        this.jTextField_elemento.setText("");
        this.jLabel_tamanio.setText("Tamaño: " + lista.getTamanio());
    }//GEN-LAST:event_jButton_eliminar_posicionMouseClicked

    private void jButton_busca_posicionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_busca_posicionMouseClicked
        // TODO add your handling code here:
        int posicion = Integer.parseInt(this.jSpinner_posicion.getValue().toString());
        String elemento = lista.BuscarPosicion(posicion);
        
        if (elemento.equals("")) {
            JOptionPane.showMessageDialog(this, "No se puedo encontrar");
            this.jList_lista.clearSelection();
        } else {
            this.jList_lista.setSelectedValue(elemento, rootPaneCheckingEnabled);
        }
        this.jTextField_elemento.setText("");
    }//GEN-LAST:event_jButton_busca_posicionMouseClicked

