    private void jButton_agregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_agregarMouseClicked
        // TODO add your handling code here:
        String elemento = this.jTextField_elemento.getText();
        lista.Agregar(elemento);
        
        model_lista.addElement(elemento);
        this.jTextField_elemento.setText("");
        this.jLabel_tamanio.setText("Tamaño: " + lista.getTamanio());
    }//GEN-LAST:event_jButton_agregarMouseClicked

    private void jButton_eliminar_elementoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_eliminar_elementoMouseClicked
        // TODO add your handling code here:
        String elemento = this.jTextField_elemento.getText();
        int posicion = lista.EliminarElemento(elemento);
        
        if (posicion == -1) {
            JOptionPane.showMessageDialog(this, "No se puedo eliminar");
        } else {
            model_lista.remove(posicion);
        }
        this.jTextField_elemento.setText("");
        this.jLabel_tamanio.setText("Tamaño: " + lista.getTamanio());
    }//GEN-LAST:event_jButton_eliminar_elementoMouseClicked

    private void jButton_buscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_buscarMouseClicked
        // TODO add your handling code here:
        String elemento = this.jTextField_elemento.getText();
        int posicion = lista.BuscarElemento(elemento);
        
        if (posicion == -1) {
            JOptionPane.showMessageDialog(this, "No se puedo encontrar");
            this.jList_lista.clearSelection();
        } else {
            this.jList_lista.setSelectedIndex(posicion);
        }
        this.jTextField_elemento.setText("");
    }//GEN-LAST:event_jButton_buscarMouseClicked

